// Generates public/fonts/pretendard.css from Pretendard's dynamic-subset CSS.
//
// Why: Pretendard renders ~4-10% smaller than the previous Gothic A1 at the same
// font-size. `size-adjust` scales the glyphs up WITHOUT scaling font-size — so text
// gets bigger but padding/margins/layout stay put (no page "zoom"). size-adjust is a
// @font-face descriptor, so we must self-host the CSS to inject it; the woff2 files
// still load on-demand (unicode-range subsetting) straight from jsdelivr.
//
// Tune SIZE_ADJUST and re-run: `node scripts/build-pretendard-css.mjs`

import { mkdirSync, writeFileSync } from "node:fs";

const VERSION = "1.3.9";
const SIZE_ADJUST = "106%"; // 100% = Pretendard's native size; raise to enlarge glyphs.
const KEEP_WEIGHTS = new Set(["400", "500", "600", "700", "800"]); // weights the app actually uses

const SRC_CDN = `https://cdn.jsdelivr.net/gh/orioncactus/pretendard@${VERSION}/dist/web/static/pretendard-dynamic-subset.css`;
const ABS_PREFIX = `url(https://cdn.jsdelivr.net/gh/orioncactus/pretendard@${VERSION}/packages/`;

const res = await fetch(SRC_CDN);
if (!res.ok) throw new Error(`Fetch failed: HTTP ${res.status}`);
const css = await res.text();

const blocks = css.match(/@font-face\s*\{[^}]*\}/g) ?? [];
const kept = blocks
  .filter(b => {
    const w = b.match(/font-weight:\s*(\d+)/);
    return w && KEEP_WEIGHTS.has(w[1]);
  })
  .map(b =>
    b
      // relative woff/woff2 paths -> absolute jsdelivr URLs (self-hosted CSS can't resolve them)
      .replace(/url\(\.\.\/\.\.\/\.\.\/packages\//g, ABS_PREFIX)
      // inject the glyph-scaling descriptor into every face
      .replace(/@font-face\s*\{/, `@font-face {\n\tsize-adjust: ${SIZE_ADJUST};`),
  );

const header =
  `/* Pretendard ${VERSION} dynamic-subset — self-hosted with size-adjust: ${SIZE_ADJUST}.\n` +
  `   Glyphs render larger without scaling font-size (layout/spacing unchanged); woff2\n` +
  `   chunks still load on-demand from jsdelivr via unicode-range. Weights: ${[...KEEP_WEIGHTS].join(", ")}.\n` +
  `   GENERATED by scripts/build-pretendard-css.mjs — do not edit by hand. */\n`;

mkdirSync("public/fonts", { recursive: true });
writeFileSync("public/fonts/pretendard.css", header + kept.join("\n") + "\n");

console.log(`Wrote public/fonts/pretendard.css — ${kept.length}/${blocks.length} faces kept, size-adjust ${SIZE_ADJUST}`);
